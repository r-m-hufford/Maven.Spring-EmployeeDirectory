CREATE TABLE EMPLOYEE (
  ID NUMBER(10,0) NOT NULL AUTO_INCREMENT,
  EMPLOYEE_NUMBER VARCHAR(255) DEFAULT NULL,
  FIRST_NAME VARCHAR2(50) DEFAULT NULL,
  LAST_NAME VARCHAR2(50) DEFAULT NULL,
  TITLE VARCHAR(50) DEFAULT NULL,
  PHONE_NUMBER VARCHAR(12) DEFAULT NULL,
  EMAIL VARCHAR(255) DEFAULT NULL,
  HIRE_DATE VARCHAR(20) DEFAULT NULL,
  MANAGER_ID BIGINT DEFAULT NULL,
  DEPT_NUM BIGINT DEFAULT NULL,

  PRIMARY KEY (ID));

CREATE TABLE DEPARTMENT (
  ID NUMBER(10,0) NOT NULL AUTO_INCREMENT,
  DEPT_NUM BIGINT DEFAULT NULL,
  DEPT_NAME VARCHAR(50) DEFAULT NULL,
  MANAGER_ID BIGINT DEFAULT NULL,
  PRIMARY KEY (ID));

CREATE TABLE JOINS (
    ID NUMBER(10,0) NOT NULL AUTO_INCREMENT,
    EMPLOYEE_NUMBER BIGINT DEFAULT NULL,
    MANAGER_ID BIGINT DEFAULT NULL,
    PRIMARY KEY (ID));

